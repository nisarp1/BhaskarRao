document.addEventListener('DOMContentLoaded', function() {
    var wardModal = document.getElementById('wardModal');
    if (wardModal) {
        wardModal.addEventListener('show.bs.modal', function(event) {
            var button = event.relatedTarget;
            var ward = button.getAttribute('data-ward');
            var activities = JSON.parse(button.getAttribute('data-activities'));
            
            var modalTitle = wardModal.querySelector('.modal-title');
            var modalWardLabel = wardModal.querySelector('#wardNameLabel');
            var activitiesList = wardModal.querySelector('#wardActivitiesList');
            
            modalTitle.textContent = ward + ' Development';
            modalWardLabel.textContent = ward;
            
            activitiesList.innerHTML = '';
            activities.forEach(function(activity) {
                var li = document.createElement('li');
                li.className = 'd-flex align-items-start';
                li.innerHTML = '<i class="feather icon-feather-arrow-right icon-small me-15px text-brand-orange mt-5px"></i><span>' + activity + '</span>';
                activitiesList.appendChild(li);
            });
        });
    }
});
</script>

